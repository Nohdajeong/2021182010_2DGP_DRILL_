turtle.penup()
turtle.goto(-250,250)
turtle.pendown()


count1 = 5
while (count1 > 0):
    count2 = 4
    while(count2 > 0):
         turtle.forward(100)
         turtle.right(90)
         count2-=1
    turtle.forward(100)
    count1-=1
    

